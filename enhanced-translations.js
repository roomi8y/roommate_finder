// Function to set Arabic as default language
function setArabicAsDefault() {
    // Set HTML lang attribute and direction
    document.documentElement.lang = 'ar';
    document.documentElement.dir = 'rtl';
    
    // Apply Arabic translations
    translateToArabic();
    
    // Update language toggle button text
    const languageToggle = document.getElementById('languageToggle');
    if (languageToggle) {
        languageToggle.innerHTML = '<i class="fas fa-globe"></i> English';
    }
    
    // Store language preference
    localStorage.setItem('preferredLanguage', 'ar');
}

// Function to translate to Arabic
function translateToArabic() {
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[key]) {
            // Store original English text if not already stored
            if (!element.hasAttribute('data-original-text')) {
                element.setAttribute('data-original-text', element.textContent.trim());
            }
            element.textContent = translations[key];
        }
    });
    
    // Also translate placeholders
    document.querySelectorAll('input[placeholder], textarea[placeholder]').forEach(element => {
        const placeholder = element.getAttribute('placeholder');
        if (translations[placeholder]) {
            element.setAttribute('placeholder', translations[placeholder]);
        }
    });
}

// Function to translate to English
function translateToEnglish() {
    document.querySelectorAll('[data-translate]').forEach(element => {
        if (element.hasAttribute('data-original-text')) {
            element.textContent = element.getAttribute('data-original-text');
        }
    });
    
    // Reset placeholders to English
    document.querySelectorAll('input[data-original-placeholder], textarea[data-original-placeholder]').forEach(element => {
        if (element.hasAttribute('data-original-placeholder')) {
            element.setAttribute('placeholder', element.getAttribute('data-original-placeholder'));
        }
    });
    
    // Update HTML attributes
    document.documentElement.lang = 'en';
    document.documentElement.dir = 'ltr';
    
    // Store language preference
    localStorage.setItem('preferredLanguage', 'en');
}

// Initialize language based on stored preference or default to Arabic
document.addEventListener('DOMContentLoaded', function() {
    // Set Arabic as default
    setArabicAsDefault();
    
    // Set up language toggle
    const languageToggle = document.getElementById('languageToggle');
    if (languageToggle) {
        languageToggle.addEventListener('click', function() {
            if (document.documentElement.lang === 'ar') {
                // Switch to English
                translateToEnglish();
                languageToggle.innerHTML = '<i class="fas fa-globe"></i> العربية';
            } else {
                // Switch to Arabic
                setArabicAsDefault();
            }
        });
    }
});
